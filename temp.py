def extract_names(data):
    """Extracts player names from the dataset."""
    names = []
    lines = data.strip().split('\n')
    for line in lines[2:]:  # Skip header lines
        parts = line.split(';')
        name = parts[0].split(' [')[0].strip('"')
        names.append(name)
    return names

def merge_and_count_unique(data1, data2):
    print("Hello")
    """Merges names from two datasets and counts unique names."""
    names1 = extract_names(data1)
    names2 = extract_names(data2)

    all_names = names1 + names2
    unique_names = []
    for name in all_names:
      if name not in unique_names:
        unique_names.append(name)
    print(unique_names)

    return len(unique_names)

